    //Creates the Method updateEmployee
    public void updateEmployee(int employeeID){
        
        this.setVisible(true);
        this.update = true;
        Employee employee = employeeCrud.read(employeeID);
        
        //Resets the fields of the form before the latest data is loaded.
        resetFields();
        
        //Loads the values of the corresponding variables into the 
        //text boxes
        txtEmployeeID.setText(String.valueOf(employee.getEmployeeID()));
        txtFirstName.setText(employee.getFirstName());
        txtLastName.setText(employee.getLastName());
        txtAccessLevel.setText(employee.getAccessLevel());
        txtAddress.setText(employee.getAddress());
        txtDob.setText(employee.getDob().toString());
        txtEmail.setText(employee.getEmail());
        txtContactNum.setText(employee.getContactNum());

    }
    
    //Displays the form for inputting data when the createEmployee method is called
    //the update boolean variable is set to false to let the program know it is creating
    //a new employee
    public void createEmployee(){
        this.setVisible(true);
        this.update = false;
       
        //Resets the form
        resetFields();
        
    }
    
