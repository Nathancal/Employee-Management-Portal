    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        //When the save button is clicked if update has been set to true then the statement will apply an update
        //However if update is set to false the else statement will create a new employee.
        if(update){
            Employee employee = new Employee(Integer.valueOf(txtEmployeeID.getText()),txtFirstName.getText(), txtLastName.getText(), txtAccessLevel.getText(), txtAddress.getText(), new Date(System.currentTimeMillis()), txtEmail.getText(), txtContactNum.getText());
            employeeCrud.update(employee);
            employeeLauncher.refresh();
        }
        else{ //not an update so we create a new employee, the employeeID is autoincremented
            Employee employee = new Employee(0,txtFirstName.getText(), txtLastName.getText(), txtAccessLevel.getText(), txtAddress.getText(), new Date(System.currentTimeMillis()), txtEmail.getText(), txtContactNum.getText());
            System.out.println(employee);
            //
            employeeCrud.create(employee);
            employeeLauncher.refresh();
        }
        this.setVisible(false);
    }//GEN-LAST:event_btnSaveActionPerformed

